'''
Dada la siguiente lista = [1,2,5,25,33,56,75,21,56,89,43,13,62,24]'
Mostrar mediante el método "print" y el operador "in", si el número 21 está en la lista
'''

lista = [1,2,5,25,33,56,75,89,43,13,62,24]

for i in lista:
    if i==  21:
        print("El número 21 está en la lista")
    else:
        print ("El número 21 no está en la lista")